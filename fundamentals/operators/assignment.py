# Assigning Values
year = 2019
print(year)

year = 2020
print(year)

year = year + 1  # Using the existing value to create a new one
print(year)

first = 20
second = first
first = 35  # Updating 'first'

print(first, second)  # 'second' remains unchanged

# The Other Operators
num = 10
print(num)

num += 5
print(num)

num -= 5
print(num)

num *= 2
print(num)

num /= 2
print(num)

num **= 2
print(num)

